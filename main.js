// ==================== QUIZ ====================
function renderQuizCats() {
  document.getElementById("quiz-cats").innerHTML = quizCatDefs
    .map(
      (c) => `
          <div class="quiz-cat-card${selectedCat === c.id ? " selected" : ""}" onclick="selectCat('${c.id}')">
            <div class="qcc-icon">${c.icon}</div>
            <div class="qcc-title">${c.title}</div>
            <div class="qcc-count">${c.count}</div>
          </div>
        `,
    )
    .join("");
}

function selectCat(id) {
  selectedCat = id;
  renderQuizCats();
  startQuiz(id);
}

function startQuiz(id) {
  currentQs = quizData[id] || [];
  currentQ = 0;
  score = 0;
  answered = false;
  const box = document.getElementById("quiz-box");
  box.classList.add("active");
  document.getElementById("result-box").classList.remove("show");
  box.scrollIntoView({ behavior: "smooth", block: "start" });
  renderQuestion();
}

function renderQuestion() {
  if (currentQ >= currentQs.length) {
    showResult();
    return;
  }
  const q = currentQs[currentQ];
  const pct = Math.round((currentQ / currentQs.length) * 100);
  document.getElementById("qp-fill").style.width = pct + "%";
  document.getElementById("qp-text").textContent =
    currentQ + 1 + " / " + currentQs.length;
  answered = false;
  document.getElementById("quiz-area").innerHTML = `
          <div class="question-num">السؤال ${currentQ + 1} من ${currentQs.length}</div>
          <div class="question-text">${q.q}</div>
          <div class="answers">${q.a
            .map(
              (ans, i) => `
            <button class="answer-btn" id="ab-${i}" onclick="checkAnswer(${i})">
              <div class="answer-letter">${["أ", "ب", "ج", "د"][i]}</div>
              <div class="answer-text">${ans}</div>
            </button>`,
            )
            .join("")}</div>
          <div class="quiz-feedback" id="qfb"></div>
          <button class="next-btn" id="next-btn" onclick="nextQuestion()">
            ${currentQ + 1 < currentQs.length ? "السؤال التالي ←" : "عرض النتيجة 🏆"}
          </button>`;
}

function checkAnswer(i) {
  if (answered) return;
  answered = true;
  const q = currentQs[currentQ];
  document.querySelectorAll(".answer-btn").forEach((b) => (b.disabled = true));
  const fb = document.getElementById("qfb");
  if (i === q.correct) {
    document.getElementById("ab-" + i).classList.add("correct");
    score++;
    fb.className = "quiz-feedback show correct-fb";
    fb.innerHTML = "✅ إجابة صحيحة!<br>" + q.exp;
  } else {
    document.getElementById("ab-" + i).classList.add("wrong");
    document.getElementById("ab-" + q.correct).classList.add("correct");
    fb.className = "quiz-feedback show wrong-fb";
    fb.innerHTML = "❌ الإجابة الصحيحة: " + q.a[q.correct] + "<br>" + q.exp;
  }
  document.getElementById("next-btn").classList.add("show");
}

function nextQuestion() {
  currentQ++;
  renderQuestion();
}

function showResult() {
  document.getElementById("quiz-area").innerHTML = "";
  const rb = document.getElementById("result-box");
  rb.classList.add("show");
  const pct = Math.round((score / currentQs.length) * 100);
  let icon = "📖",
    msg = "محتاجين نشد شوية، راجع المادة تاني";
  if (pct >= 80) {
    icon = "🏆";
    msg = "عظيييييم! مبهورين بيك يا نجم 🌟";
  } else if (pct >= 60) {
    icon = "⭐";
    msg = "جيد جداً! استمر في الدراسة لتحقق التميز";
  }
  document.getElementById("result-icon").textContent = icon;
  document.getElementById("result-score").textContent =
    score + " / " + currentQs.length;
  document.getElementById("result-msg").textContent = msg;
  document.getElementById("qp-fill").style.width = "100%";
  document.getElementById("qp-text").textContent =
    currentQs.length + " / " + currentQs.length;
}

function restartQuiz() {
  if (selectedCat) startQuiz(selectedCat);
}

