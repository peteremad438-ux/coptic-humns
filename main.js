const audioEngine = document.getElementById("audio-engine");

// ==================== RENDER ====================
function renderHymns(sectionKey) {
  const hymns = hymnData[sectionKey] || [];
  const grid = document.getElementById("grid-" + sectionKey);
  if (!grid) return;
  grid.innerHTML = hymns
    .map(
      (h, i) => `
          <div class="hymn-card${playingId === h.id ? " playing" : ""}" id="hc-${h.id}" onclick="playHymn('${h.id}','${sectionKey}',${i})">
            <div class="hymn-num">${i + 1}</div>
            <div class="hymn-cat">${h.cat}</div>
            <div class="hymn-name">${h.name}</div>
            <div class="hymn-sub">${h.sub}</div>
            <div class="hymn-controls">
              <button class="play-btn">${playingId === h.id && playing ? "⏸" : "▶"}</button>
              <div class="progress-wrap" onclick="seekFromCard(event,'${h.id}')">
                <div class="progress-fill" id="prog-${h.id}" style="width:${playingId === h.id ? cardPct() : "0"}%"></div>
              </div>
              <span class="hymn-dur">${h.dur}</span>
              ${playingId === h.id && playing ? '<div class="wave-bars"><span></span><span></span><span></span><span></span></div>' : ""}
            </div>
          </div>
        `,
    )
    .join("");
}

function cardPct() {
  if (!audioEngine.duration) return 0;
  return (audioEngine.currentTime / audioEngine.duration) * 100;
}

function renderAllGrids() {
  Object.keys(hymnData).forEach((key) => renderHymns(key));
}

// ==================== PLAY ====================
function playHymn(id, sectionKey, idx) {
  const hymn = hymnData[sectionKey][idx];
  currentSection = sectionKey;
  currentSectionHymns = hymnData[sectionKey];
  currentIdx = idx;

  if (playingId === id) {
    if (audioEngine.paused) {
      audioEngine.play();
      playing = true;
    } else {
      audioEngine.pause();
      playing = false;
    }
  } else {
    playingId = id;
    audioEngine.pause();
    if (hymn.src) {
      audioEngine.src = hymn.src;
      audioEngine.load();
      audioEngine.play().catch(() => {});
      playing = true;
    } else {
      playing = true;
    }
  }

  document.getElementById("pb-title").textContent =
    hymn.name + " — " + hymn.cat;
  document.getElementById("pb-play").textContent = playing ? "⏸" : "▶";
  document.getElementById("player-bar").classList.add("visible");
  updateTotalTime();
  renderAllGrids();
}

function togglePlay() {
  if (
    audioEngine.src &&
    !audioEngine.src.endsWith("#") &&
    !audioEngine.src.endsWith("/")
  ) {
    if (audioEngine.paused) {
      audioEngine.play();
      playing = true;
    } else {
      audioEngine.pause();
      playing = false;
    }
  } else {
    playing = !playing;
  }
  document.getElementById("pb-play").textContent = playing ? "⏸" : "▶";
  renderAllGrids();
}

function prevHymn() {
  currentIdx =
    (currentIdx - 1 + currentSectionHymns.length) % currentSectionHymns.length;
  const h = currentSectionHymns[currentIdx];
  playHymn(h.id, currentSection, currentIdx);
}
function nextHymn() {
  currentIdx = (currentIdx + 1) % currentSectionHymns.length;
  const h = currentSectionHymns[currentIdx];
  playHymn(h.id, currentSection, currentIdx);
}

function advanceAudio(s) {
  if (audioEngine.duration)
    audioEngine.currentTime = Math.max(
      0,
      Math.min(audioEngine.currentTime + s, audioEngine.duration),
    );
}

// === SEEK FROM PLAYER BAR ===
function seekFromBar(e) {
  if (!audioEngine.duration) return;
  const bar = document.getElementById("player-progress");
  const rect = bar.getBoundingClientRect();
  // RTL: click on left = end, click on right = start
  const ratio = 1 - (e.clientX - rect.left) / rect.width;
  audioEngine.currentTime = Math.max(
    0,
    Math.min(ratio * audioEngine.duration, audioEngine.duration),
  );
}

// === SEEK FROM CARD PROGRESS BAR ===
function seekFromCard(e, id) {
  e.stopPropagation();
  if (playingId !== id || !audioEngine.duration) return;
  const bar = e.currentTarget;
  const rect = bar.getBoundingClientRect();
  const ratio = 1 - (e.clientX - rect.left) / rect.width;
  audioEngine.currentTime = Math.max(
    0,
    Math.min(ratio * audioEngine.duration, audioEngine.duration),
  );
}

function updateTotalTime() {
  const onMeta = () => {
    const d = audioEngine.duration;
    if (d && isFinite(d)) {
      const m = Math.floor(d / 60);
      const s = Math.floor(d % 60)
        .toString()
        .padStart(2, "0");
      document.getElementById("pb-time-total").textContent = m + ":" + s;
    }
    audioEngine.removeEventListener("loadedmetadata", onMeta);
  };
  audioEngine.addEventListener("loadedmetadata", onMeta);
}

audioEngine.addEventListener("ended", () => {
  playing = false;
  document.getElementById("pb-play").textContent = "▶";
  renderAllGrids();
});

audioEngine.addEventListener("timeupdate", () => {
  if (!audioEngine.duration) return;
  const pct = (audioEngine.currentTime / audioEngine.duration) * 100;
  document.getElementById("pb-progress").style.width = pct + "%";
  const progCard = document.getElementById("prog-" + playingId);
  if (progCard) progCard.style.width = pct + "%";
  const m = Math.floor(audioEngine.currentTime / 60);
  const s = Math.floor(audioEngine.currentTime % 60)
    .toString()
    .padStart(2, "0");
  document.getElementById("pb-time-current").textContent = m + ":" + s;
});

